export const DEMO_TODAY = new Date('2026-06-20T08:00:00Z')

export function daysAgo(n: number): string {
  return new Date(DEMO_TODAY.getTime() - n * 86400000).toISOString()
}

/** V3 Phase 0 defaults — tier/qualification content arrives in later phases. */
function v3Defaults(updatedAt: string, opts?: { activeSince?: string | null }) {
  return {
    tier: null,
    tierRationale: '',
    autoTiered: false,
    rewardCategory: null,
    ehsCoordinatorId: null,
    qualification: null,
    readiness: null,
    activeSince: opts?.activeSince ?? null,
    lastActivityAt: updatedAt,
    sponsorDecision: null,
    sponsorDecisionNote: '',
    intakeMode: 'manual' as const,
  } as const
}

function stageStatus(
  overrides: Partial<Record<LifecycleStage, StageStatus>>,
): Record<LifecycleStage, StageStatus> {
  return {
    Assessment: 'NotStarted',
    Policy: 'NotStarted',
    SupplierOversight: 'NotStarted',
    Development: 'NotStarted',
    Deployment: 'NotStarted',
    Use: 'NotStarted',
    Improvement: 'NotStarted',
    Decommissioning: 'NotStarted',
    Enablement: 'InProgress',
    ...overrides,
  }
}

function transition(
  partial: Omit<StageTransition, 'id'> & { id?: string },
  index: number,
): StageTransition {
  return {
    id: partial.id ?? `trn-${partial.projectId}-${String(index).padStart(3, '0')}`,
    projectId: partial.projectId,
    fromStage: partial.fromStage,
    toStage: partial.toStage,
    fromStatus: partial.fromStatus,
    toStatus: partial.toStatus,
    actorUserId: partial.actorUserId,
    actorRole: partial.actorRole,
    timestamp: partial.timestamp,
    note: partial.note,
  }
}

