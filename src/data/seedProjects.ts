function assessmentBundle(opts: {
  tier: 'Tier1' | 'Tier2' | 'Tier3'
  risk: 'Low' | 'Medium' | 'High'
  reward: 'Kaizen' | 'TeamProject' | 'ManagementInitiative' | 'Innovation'
  rationale: string
  primaryTrue?: number
}) {
  const primaryTrue = opts.primaryTrue ?? 2
  return {
    readiness: {
      feasibility: filledBools(7, 5),
      viability: filledBools(7, 5),
      desirability: filledBools(7, 4),
    },
    qualification: {
      primary: filledBools(6, primaryTrue),
      supporting: [true, true, false, true],
      exclusions: filledBools(5, 0),
      riskTier: opts.risk,
    },
    tier: opts.tier,
    tierRationale: opts.rationale,
    rewardCategory: opts.reward,
    autoTiered: false,
  }
}

function stageStatus(
  overrides: Partial<Record<LifecycleStage, StageStatus>>,
): Record<LifecycleStage, StageStatus> {
  return {
    Assessment: 'NotStarted',
    Policy: 'NotStarted',
    SupplierOversight: 'NotStarted',
    Development: 'NotStarted',
    Deployment: 'NotStarted',
    Use: 'NotStarted',
    Improvement: 'NotStarted',
    Decommissioning: 'NotStarted',
    Enablement: 'InProgress',
    ...overrides,
  }
}

function transition(
  partial: Omit<StageTransition, 'id'> & { id?: string },
  index: number,
): StageTransition {
  return {
    id: partial.id ?? `trn-${partial.projectId}-${String(index).padStart(3, '0')}`,
    projectId: partial.projectId,
    fromStage: partial.fromStage,
    toStage: partial.toStage,
    fromStatus: partial.fromStatus,
    toStatus: partial.toStatus,
    actorUserId: partial.actorUserId,
    actorRole: partial.actorRole,
    timestamp: partial.timestamp,
    note: partial.note,
  }
}

